"""
Utility functions for mathematical operations.
Separating logic from UI and tests for better maintainability.
"""

def square(n: int) -> int:
    """Calculate the square of an integer."""
    return n ** 2

def cube(n: int) -> int:
    """Calculate the cube of an integer."""
    return n ** 3

def fifth_power(n: int) -> int:
    """Calculate the fifth power of an integer."""
    return n ** 5
